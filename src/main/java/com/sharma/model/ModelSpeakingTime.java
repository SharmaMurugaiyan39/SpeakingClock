package com.sharma.model;

public class ModelSpeakingTime {

    String resultWord;

    String resultTimeZone;

    public ModelSpeakingTime(String resultWord, String resultTimeZone) {
        this.resultWord = resultWord;
        this.resultTimeZone = resultTimeZone;
    }


    public String getResultWord() {
        return resultWord;
    }

    public void setResultWord(String resultWord) {
        this.resultWord = resultWord;
    }

    public String getResultTimeZone() {
        return resultTimeZone;
    }

    public void setResultTimeZone(String resultTimeZone) {
        this.resultTimeZone = resultTimeZone;
    }





}
